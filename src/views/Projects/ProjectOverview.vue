<script>
export default {
  name: 'ProjectOverview',
  data: () => ({
    project: {},
    user: {},
    discussion_title: '',
    discussions: [],
    message: '',
    file: '',
    fileName: '',
    attachments: [],
    title: '',
    tasks: []
  }),
  async mounted() {
    if (!this.$store.state.user) {
      await this.$store.dispatch('getUser', localStorage.getItem('userId'));
      this.user = this.$store.state.user;
    }
    await this.$store.dispatch('getProject', this.$route.params.id)
    this.project = this.$store.state.project
    this.getDiscussions()
    this.getAttachments()
    this.getTasks()
  },
  methods: {
    async compleateTask(task) {
      await this.$store.dispatch('updateTask', {
        id: this.$route.params.id,
        task_id: task._id,
        title: task.title,
        compleated: true
      })
      this.getTasks()
    },
    async addTask() {
      await this.$store.dispatch('newTask', {
        title: this.title,
        id: this.$route.params.id
      })
      this.getTasks()
      this.title = ''
    },
    async getTasks() {
      await this.$store.dispatch('getTasks', this.$route.params.id)
      this.tasks = this.$store.state.tasks
    },
    async deleteTask(task_id) {
      await this.$store.dispatch('deleteTask', {
        id: this.$route.params.id,
        task_id: task_id
      })
      this.getTasks()
    },
    async getAttachments() {
      await this.$store.dispatch('getAttachments', this.$route.params.id)
      this.attachments = this.$store.state.attachments
    },
    async getDiscussions() {
      await this.$store.dispatch('getDiscussions', this.$route.params.id)
      this.discussions = this.$store.state.discussions
    },
    async createDiscussion() {
      await this.$store.dispatch('newDiscussion', {
        title: this.discussion_title,
        user_id: this.user._id,
        id: this.$route.params.id,
      })
      this.discussion_title = ''
      this.getDiscussions()
    },
    async deleteDiscussion(id) {
      await this.$store.dispatch('deleteDiscussion', {
        id: this.$route.params.id,
        discussion_id: id,
      })
      this.getDiscussions()
    },
    async addMessage(discussion_id) {
      await this.$store.dispatch('newMessage', {
        id: this.$route.params.id,
        discussion_id: discussion_id,
        user_id: this.user._id,
        message: this.message,
      })
      this.message = ''
      this.getDiscussions()
    },
    handleFile(e) {
      const selectedFile = e.target.files[0]
      this.file = selectedFile
      this.fileName = selectedFile.name
    },
    async download(id) {
      const file = this.attachments.find(attachment => attachment._id === id)
      const newFile = new Blob([file.file], {type: file.type})
      const url = window.URL.createObjectURL(newFile)
      const link = document.createElement('a')
      link.href = url
      link.setAttribute('download', file.name)
      document.body.appendChild(link)
      link.click()
      document.body.removeChild(link)
    },
    addFile() {
      const reader = new FileReader()
      reader.readAsDataURL(this.file)
      reader.onload = async () => {
        const newfile = reader.result.split(',')[1]
        await this.$store.dispatch('newAttachments', {
          id: this.$route.params.id,
          name: this.fileName,
          file: newfile,
        })
        this.file = ''
        this.fileName = ''
        this.getAttachments()
      }
    },
    async deleteAttachment(id) {
      await this.$store.dispatch('deleteAttachment', {
        id: this.$route.params.id,
        attachment_id: id,
      })
      this.getAttachments()
    }
  }
}
